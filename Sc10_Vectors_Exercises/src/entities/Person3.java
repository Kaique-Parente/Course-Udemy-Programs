package entities;

public class Person3 {
    private double altura;
    private char gender;

    public Person3(double altura, char gender) {
        this.altura = altura;
        this.gender = gender;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

    public char getGender() {
        return gender;
    }

    public void setGender(char gender) {
        this.gender = gender;
    }
    
    
}
